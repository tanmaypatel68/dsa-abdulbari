#include<iostream>
using namespace std;
int main(){
    int a[5];
    int b[5]={1,2,3,4,5};
    int c[5]={1,2};
    int d[5]={0};
    int e[]={1,2,3,4,5};
    
    for(int i=0;i<5;i++){
        cout<<a[i]<<" ";
    }
    cout<<endl;
    for(int i=0;i<5;i++){
        cout<<b[i]<<" ";
    }
    cout<<endl;
    for(int i=0;i<5;i++){
        cout<<c[i]<<" ";
    }
    cout<<endl;
    for(int i=0;i<5;i++){
        cout<<d[i]<<" ";
    }
    cout<<endl;
    for(int i=0;i<5;i++){
        cout<<e[i]<<" ";
    }
    cout<<endl;
    return 0;
}

